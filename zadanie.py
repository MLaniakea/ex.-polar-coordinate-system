# импортируем библиотеку math
import math

# ввод выбранного действия
choice = int(input("Выберите действие (1 - перевод из полярной в декартовую, 2 - перевод из декартовой в полярную): "))

if choice == 1:
    # ввод радиуса и угла
    r = float(input("Введите радиус: "))
    p_deg = float(input("Введите угол в градусах: "))

    # переводим угол из градусов в радианы
    p = math.radians(p_deg)

    # вычисляем координаты x и y
    x = r * math.cos(p)
    y = r * math.sin(p)

    # вывод результатов
    print("Координата x:", x)
    print("Координата y:", y)

elif choice == 2:
    # ввод координат x и y
    x = float(input("Введите координату x: "))
    y = float(input("Введите координату y: "))

    # вычисляем радиус и угол
    r = math.sqrt(x**2 + y**2)
    p = math.atan2(y, x)

    # переводим угол из радиан в градусы
    p_deg = math.degrees(p)

    # вывод результатов
    print("Радиус:", r)
    print("Угол в градусах:", p_deg)

else:
    print("Неверный выбор действия")

